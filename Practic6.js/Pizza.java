public class Pizza {
    public String Size;
    public String Sauce;
    public String Cheese;

    public void setCheese(String cheese) {
        Cheese = cheese;
    }

    public void setSize(String size) {
        Size = size;
    }

    public void setSauce(String sauce) {
        Sauce = sauce;
    }

    public String getCheese() {
        return Cheese;
    }

    public String getSauce() {
        return Sauce;
    }

    public String getSize() {
        return Size;
    }
}
